package myPackage;

public class DataTypes {

	public static void main(String[] args) {

		//Primitive
		int a = 10;
		double d = 10.5;
		char c = 'A';
		boolean b = false;
		
		//Derived
		String s = "Hello";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		
		System.out.println(s);

	}

}
